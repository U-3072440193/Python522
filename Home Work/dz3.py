k = int(input("Введите сумму в копейках от 1 до 99 : "))
if 1 <= k <= 99:
    if k == 1 or k % 10 == 1:
        print(k, "копейка")
    elif 2 <= k <= 4 or 12 <= k <= 14 or 2 <= k % 10 <= 4:
        print(k, "копейки")
    else:
        print(k, "копеек")
elif 1 > k:
    print("У вас недостаточно средств")
else:
    print("Некорректно введены данные")
